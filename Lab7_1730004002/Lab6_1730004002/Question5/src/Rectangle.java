
public class Rectangle extends Shape {
	private double width;
	private double length;
	public Rectangle(double x, double y, double width,double length) {
		super(x,y);
		this.width=width;
		this.length=length;
	}
	@Override
	public double area() {
		double area=width*length*0.5;
		return area;
	}
	public static void testRectangle() {
		Rectangle r=new Rectangle(0.0,0.0,3.0,4.0);
		System.out.println(r.getX()==0.0);
		System.out.println(r.getY()==0.0);
		System.out.println(r.area()==6.0);
		//create a new Rectangle object to test the method
	}
}
