
public class Square extends Rectangle{
	private double size;
	public Square(double x,double y,double size) {
		super(x,y,size,size);
		this.size=size;
		}
	@Override
	public double area() {
		double area;
		area=size*size;
		return area;
	}
	public static void testSquare() {
		Square s=new Square(0.0,0.0,4.0);
		System.out.println(s.getX()==0.0);
		System.out.println(s.getY()==0.0);
		System.out.println(s.area()==16.0);
		
	}
}
